CREATE TABLE customers (
    customer_id     INT AUTO_INCREMENT PRIMARY KEY,
    first_name      VARCHAR(50)     NOT NULL,
    last_name       VARCHAR(50)     NOT NULL,
    age             INT             NOT NULL,
    gender          ENUM('Male','Female','Other') NOT NULL,
    province        VARCHAR(50)     NOT NULL,
    annual_income   DECIMAL(12,2)   NOT NULL,
    employment_type ENUM('Salaried','Self-Employed','Unemployed','Retired') NOT NULL,
    credit_score    INT             NOT NULL,   -- Range 300–850
    created_at      DATETIME        DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE loans (
    loan_id         INT AUTO_INCREMENT PRIMARY KEY,
    customer_id     INT             NOT NULL,
    loan_type       ENUM('Personal','Home','Auto','Business','Education') NOT NULL,
    loan_amount     DECIMAL(14,2)   NOT NULL,
    interest_rate   DECIMAL(5,2)    NOT NULL,   -- Annual %
    tenure_months   INT             NOT NULL,
    disbursement_date DATE          NOT NULL,
    maturity_date   DATE            NOT NULL,
    loan_status     ENUM('Active','Paid','Default','Under Review') NOT NULL,
    outstanding_balance DECIMAL(14,2) NOT NULL,
    FOREIGN KEY (customer_id) REFERENCES customers(customer_id)
);

CREATE TABLE loan_payments (
    payment_id      INT AUTO_INCREMENT PRIMARY KEY,
    loan_id         INT             NOT NULL,
    payment_date    DATE            NOT NULL,
    amount_paid     DECIMAL(12,2)   NOT NULL,
    payment_status  ENUM('On-Time','Late','Missed') NOT NULL,
    FOREIGN KEY (loan_id) REFERENCES loans(loan_id)
);


